# Integer number
num = 100
print(num)
print("Data Type of variable num is", type(num))

# float number
fnum = 34.45
print(fnum)
print("Data Type of variable fnum is", type(fnum))

# complex number
cnum = 3 + 4j
print(cnum)
print("Data Type of variable cnum is", type(cnum))

